// For clássico precisa de uma variável, condição e incremento
// Primeiro cria uma variável 'let' chamanda 'i' e atribui o valor 0
// Depois, cria uma condicional que vai ser executada enquanto o valor da variável 'i' for menor ou igual a 10
// Em seguida, incrementa o valor da variável 'i' e depois imprime o valor da variável
for (let i = 0; i <= 100; i += 10) {
  console.log(`Linha: ${i}`);
}
